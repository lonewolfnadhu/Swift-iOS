import UIKit

/*
 'Data Types' in Swift
 */

// Integer
var a: Int = 40
print("This is an Integer value: \(a)")

// String
var b: String = "Nadhu"
print("This is a String value: \(b)")

// Floating Point
var c: Float = 40.123
print("This is a Floating Point value: \(c)")

// Double
var d: Double = 53.2345646363465
print("This is a Double value: \(d)")

// Boolean
var e: Bool = true
print("This is a Boolean value: \(e)")
